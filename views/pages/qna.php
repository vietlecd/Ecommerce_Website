<script>
document.addEventListener('DOMContentLoaded', function() {
    const items = document.querySelectorAll('.qna-accordion-item');
    items.forEach((item) => {
        const toggle = item.querySelector('.qna-accordion-toggle');
        const icon = toggle.querySelector('i');
        toggle.addEventListener('click', () => {
            items.forEach((el) => {
                if (el !== item) {
                    el.classList.remove('is-open');
                    const elIcon = el.querySelector('.qna-accordion-toggle i');
                    if (elIcon) {
                        elIcon.classList.remove('fa-minus');
                        elIcon.classList.add('fa-plus');
                    }
                }
            });
            item.classList.toggle('is-open');
            if (item.classList.contains('is-open')) {
                icon.classList.remove('fa-plus');
                icon.classList.add('fa-minus');
            } else {
                icon.classList.remove('fa-minus');
                icon.classList.add('fa-plus');
            }
        });
    });
});
</script>
